/* Supported languages */
const SUPPORTED_LANGS = ['nl', 'en', 'fr', 'pt'];

/* Default language detection */
function detectLanguage() {
  const saved = localStorage.getItem('amc-lang');
  if (saved && SUPPORTED_LANGS.includes(saved)) return saved;

  const browserLang = navigator.language?.slice(0, 2).toLowerCase();
  if (SUPPORTED_LANGS.includes(browserLang)) return browserLang;

  return 'nl';
}

/* Get current language */
function getCurrentLang() {
  return document.documentElement.lang || detectLanguage();
}

/* Translate a key */
function t(key, lang = getCurrentLang()) {
  const dict = TRANSLATIONS[lang] || TRANSLATIONS.nl;
  const val = dict[key];
  return val !== undefined ? val : (TRANSLATIONS.nl[key] || key);
}

/* Set language and persist */
function setLanguage(lang) {
  if (!SUPPORTED_LANGS.includes(lang)) return;
  localStorage.setItem('amc-lang', lang);
  document.documentElement.lang = lang;
  applyTranslations();
}

/* Apply all data-i18n translations */
function applyTranslations() {
  const lang = getCurrentLang();

  /* Static elements with data-i18n */
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const val = t(key, lang);
    if (val !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        if (el.hasAttribute('placeholder')) el.placeholder = val;
        else el.value = val;
      } else if (el.tagName === 'META') {
        el.setAttribute('content', val);
      } else {
        el.innerHTML = val;
      }
    }
  });

  /* Elements with data-i18n-attr (translate specific attribute) */
  document.querySelectorAll('[data-i18n-attr]').forEach(el => {
    const [attr, key] = el.dataset.i18nAttr.split(':');
    const val = t(key, lang);
    if (val !== undefined) el.setAttribute(attr, val);
  });

  /* Update aria-labels */
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const key = el.dataset.i18nAria;
    const val = t(key, lang);
    if (val !== undefined) el.setAttribute('aria-label', val);
  });

  /* Update language switcher active state */
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('is-active', btn.dataset.lang === lang);
  });

  /* Update page title */
  const titleKey = document.querySelector('title[data-i18n]');
  if (titleKey) document.title = t(titleKey.dataset.i18n, lang);

  /* Fire custom event for dynamic content */
  window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}

/* Initialize i18n on page load */
function initI18n() {
  const lang = detectLanguage();
  document.documentElement.lang = lang;
  applyTranslations();
}

/* Export for use in other modules */
if (typeof window !== 'undefined') {
  window.TRANSLATIONS = TRANSLATIONS;
  window.SUPPORTED_LANGS = SUPPORTED_LANGS;
  window.t = t;
  window.setLanguage = setLanguage;
  window.applyTranslations = applyTranslations;
  window.initI18n = initI18n;
}
